number = 30
number2 = 10

# console: The result is: 40
print("The result is: ", number + number2)   # Addition of number + number2

# console: The result is: 20
print("The result is: ", number - number2)   # Subtraction of number - number2

# console: The result is: 300
print("The result is: ", number * number2)   # Multiplication of number * number2

# console: The result is: 3.0
print("The result is: ", number / number2)   # Division of number / number2

# console: The result is: 348678440100000000000000000000
print("The result is: ", number ** number2)  # Exponentiation of number raised to number2

# console: The result is: 3
print("The result is: ", number // number2)  # Floor division of number // number2

# console: The result is: 0
print("The result is: ", number % number2)   # Modulo (remainder) of number % number2

# console: true
print(number > number2)

# console: false
print(number < number2)

